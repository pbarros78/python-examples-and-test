"""
Forma cuatro:
Sólo con while
"""
print("Cuarta Forma")

serie = 1 # primera serie
while (serie < 4):
    repeticion = 1 # primera repeticion
    print("Serie: ", serie)
    while (repeticion < 6):
        print("Repetición: ", repeticion)
        repeticion += 1 # siguiente repeticion
    serie += 1 # siguiente serie
