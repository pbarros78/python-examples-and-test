"""
Forma dos:
Se aumentan las series de manera manual
Las repeticiones con while
"""
print("Segunda Forma")

serie = 1 # primera serie
repeticion = 1 # primera repeticion
print("Serie: ", serie)
while (repeticion < 6):
    print("Repetición: ", repeticion)
    repeticion += 1 # siguiente repeticion

serie += 1 # segunda serie
repeticion = 1 # primera repeticion
print("Serie: ", serie)
while (repeticion < 6):
    print("Repetición: ", repeticion)
    repeticion += 1 # siguiente repeticion

